// Regex for letters only (including spaces)
export const nameRegex = /^[A-Za-z\s]*$/;

// Regex for numbers only
export const numberRegex = /^[0-9]*$/;

// Regex for email validation
export const emailRegex = /^[a-z0-9@.\-\_]*$/;

export const splCharRegex = /^[A-Z\d]*$/;

export const ifscRegex = /^[A-Z0-9]*$/;

export const upiRegex = /^[\w.\-@]*$/;

export const dobRegex = /^[0-9/]*$/;
